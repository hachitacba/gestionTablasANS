        principal.setVisible(false);
        this.setTitle("Eliminar registros de personas");
        
        String[] cab={"Apellido","Nombre","Edad"};
        
        modelo=new DefaultTableModel(cab,0);
        
        
        Object[] vec=new Object[3];
        
        int cant=gestor.cantPersonas();
        for(int i=0;i<cant;i++)
        {
            vec[0]=gestor.getPersona(i).getApellido();
            vec[1]=gestor.getPersona(i).getNombre();
            vec[2]=gestor.getPersona(i).getEdad();
            
            modelo.addRow(vec);
        }
        tablaPersonas.setModel(modelo);
        
        
