        int fila=tablaPersonas.getSelectedRow();
        
        String ape=(String)modelo.getValueAt(fila, 0);
        String nom=(String)modelo.getValueAt(fila,1);
        int edad=(int)modelo.getValueAt(fila, 2);
        
