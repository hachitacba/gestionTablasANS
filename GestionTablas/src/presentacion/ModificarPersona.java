    public ModificarPersona(Menu p,GestorPersonas g) {
        
        this.setTitle("Modificar personas registradas");
        initComponents();
        principal=p;
        principal.setVisible(false);
        gestor=g;
        
        int cant=gestor.cantPersonas();
        Object[][] mat=new Object[cant][5];
        Persona per=null;
        for(int i=0;i<cant;i++)
        {
            per=gestor.getPersona(i);
            if(per!=null)
            {
                mat[i][0]=per.getApellido();
                mat[i][1]=per.getNombre();
                mat[i][2]=per.getEdad();
                mat[i][3]=String.valueOf(per.getGenero());
                mat[i][4]=Arrays.toString(per.getPasatiempos());
            }
            /*mat[i][0]=gestor.getPersona(i).getApellido();
              mat[i][1]=gestor.getPersona(i).getNombre();
              mat[i][2]=gestor.getPersona(i).getEdad();
            */
        }
        modelo=(DefaultTableModel)tablaPersonas.getModel();
        Object[] cab={"Apellido","Nombre","Edad","Genero","Pasatiempo"};
        modelo.setDataVector(mat, cab);
        
        txtApellido.setEnabled(false);
        txtNombre.setEnabled(false);
        txtEdad.setEnabled(false);
        grupGenero.clearSelection();
        chkOtras.setEnabled(false);
        chkPasearMascota.setEnabled(false);
        chkPatinar.setEnabled(false);
        chkVerSeries.setEnabled(false);
        chkVideoJuegos.setEnabled(false);
        
        
    }
    
