    private void btnSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionarActionPerformed
        // TODO add your handling code here:
        txtApellido.setEnabled(true);
        txtNombre.setEnabled(true);
        txtEdad.setEnabled(true);

        grupGenero.clearSelection();
        chkOtras.setEnabled(true);
        chkPasearMascota.setEnabled(true);
        chkPatinar.setEnabled(true);
        chkVerSeries.setEnabled(true);
        chkVideoJuegos.setEnabled(true);
        rbtMasculino.setEnabled(true);
        rbtFemenino.setEnabled(true);
        
        int fila=tablaPersonas.getSelectedRow();
        if(fila>=0)
        {
            txtApellido.setText((String)modelo.getValueAt(fila, 0));
            txtNombre.setText((String)modelo.getValueAt(fila, 1));
            txtEdad.setText(String.valueOf(modelo.getValueAt(fila, 2)));
            if (String.valueOf(modelo.getValueAt(fila, 3))=="M") {
                rbtMasculino.setSelected(true);
            }else if (String.valueOf(modelo.getValueAt(fila, 3))=="F") {
                rbtFemenino.setSelected(true);
            }
                
            
            
        }
        tablaPersonas.setEnabled(false);
    }//GEN-LAST:event_btnSeleccionarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        String ape=txtApellido.getText();
        String nom=txtNombre.getText();
        int edad=Integer.parseInt(txtEdad.getText());
        char gen='x';
        if (rbtMasculino.isSelected()) {
            gen='M';
        }else if (rbtFemenino.isSelected()) {
            gen='F';
        }
        String[] pas=new String[4];
        if (chkVideoJuegos.isSelected()) {
            pas[0]="Video Juegos";
        }else if (chkPatinar.isSelected()) {
            pas[1]="Patinar";
        }else if (chkVerSeries.isSelected()) {
            pas[2]="Ver series";
        }else if (chkPasearMascota.isSelected()) {
            pas[3]="Pasear Mascota";
        }else if (chkOtras.isSelected()) {
            pas[4]="Otras";
        }
        
        
        //Persona per=new Persona(ape,nom,edad);
        int fila=tablaPersonas.getSelectedRow();
        modelo.setValueAt(ape, fila, 0);
        modelo.setValueAt(nom,fila,1);
        modelo.setValueAt(edad,fila,2);
        modelo.setValueAt(gen, fila, 3);
        
        gestor.modificarPersona(fila,ape,nom,edad,gen,pas);
        
        limpiar();
        
        
    }//GEN-LAST:event_btnModificarActionPerformed

